<template>
  <carousel :interval="8000">
    <carousel-indicators>
      <carousel-indicator :index="0" :active="show[0]"></carousel-indicator>
      <carousel-indicator :index="1" :active="show[1]"></carousel-indicator>
      <carousel-indicator :index="2" :active="show[2]"></carousel-indicator>
    </carousel-indicators>
    <carousel-inner>
      <carousel-item :active="show[0]" src="https://mdbootstrap.com/img/Photos/Slides/img%20(130).jpg" alt="First slide"></carousel-item>
      <carousel-item :active="show[1]" src="https://mdbootstrap.com/img/Photos/Slides/img%20(129).jpg" alt="Seccond slide"></carousel-item>
      <carousel-item :active="show[2]" src="https://mdbootstrap.com/img/Photos/Slides/img%20(70).jpg" alt="Third slide"></carousel-item>
    </carousel-inner>
    <carousel-navigation></carousel-navigation>
  </carousel>
</template>

<script>
import Carousel from '@/components/Carousel';
import CarouselIndicators from '@/components/CarouselIndicators';
import CarouselIndicator from '@/components/CarouselIndicator';
import CarouselInner from '@/components/CarouselInner';
import CarouselItem from '@/components/CarouselItem';
import CarouselNavigation from '@/components/CarouselNavigation';

export default {
  name: 'CarouselPage',
  components: {
    Carousel,
    CarouselIndicators,
    CarouselIndicator,
    CarouselInner,
    CarouselItem,
    CarouselNavigation
  },
  data() {
    return {
      page: 0,
      items: '',
      interval: 10000,
      show: { 0: true, 1: false, 2: false, 3: false, 4: false, 5: false, 6: false, 7: false, 8: false, 9: false }
    };
  },
  methods: {
    slidePage(target) {
      if (target == 'next') {
        if (this.page < this.items) {
          this.show[this.page] = false;
          this.page++;
          this.show[this.page] = true;
        } else {
          this.show[this.page] = false;
          this.page = 0;
          this.show[this.page] = true;
        }
      } else if (target == 'prev') {
        if (this.page == 0) {
          this.show[this.page] = false;
          this.page = this.items;
          this.show[this.page] = true;
        } else {
          this.show[this.page] = false;
          this.page--;
          this.show[this.page] = true;
        }
      } else {
        this.show[this.page] = false;
        this.page = target;
        this.show[this.page] = true;
      }
    },
    autoSlide() {
      this.slide = setInterval(() => {
        this.slidePage('next');
      }, this.interval);
    }
  },
  created() {
    this.$on('changeSlide', function(showSlide) {
      this.slidePage(showSlide.slideIndex);
      clearInterval(this.slide);
      this.autoSlide();
    });
    this.$on('defineInterval', function(defineInterval) {
      this.interval = defineInterval.newInterval;
    });
  },
  mounted() {
    this.items = this.$el.querySelectorAll('img').length - 1;
    this.autoSlide();
  },
};
</script>

